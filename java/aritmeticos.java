public class aritmeticos{
	static int a = 7;
	static int b = 17;
	static int c = 9;


	public static void main(String[] args) {
		System.out.println("Sumamos a + b y es igual a " + (a + b));
		System.out.println("Restamos c - a y es igual a " + (c - a));
		System.out.println("Multiplicamos b * c y es igual a " + (b * c));
		System.out.println("Dividimos c / a y es igual a " + (c / a));
		System.out.println("b % a es igual a " + (b % a));
		System.out.println("c++ es igual a " + (c++));
		System.out.println("c++ es igual a " + (c));
		System.out.println("b-- es igual a " + (b--));
		System.out.println("b-- es igual a " + (b));
	}

}