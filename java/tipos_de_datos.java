/*Tipos de modificadores
Default: sino se elige modificador se usa este por defecto
Private: solo donde esta declarado lo usas. Sólo en una clase.
Protected: Lo usas donde estas declarado y puede ser utilizado por otra clase si la nombra.
*/

/*Tipos de variables
Variables locales: las declaras en una parte del código y sólo ahí la puedes usar.
Variables de instanciao globales, Podras hacer uso de ellas durante todo el código
P. Ej: objeto.nombre
Variables de clase: Son estáticas, y también pueden usarse durante todo el código.
Siempre debe llevar Static. Solo llamas a la variables. P. ej: PI.
*/
